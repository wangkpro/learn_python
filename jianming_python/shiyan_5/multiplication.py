#!/usr/bin/env python3
i = 1 #定义i的初始值为1
print("-" * 50) #打印50个-符号
while i < 11:#当i小于11时会一直循环缩进内的代码
    n = 1#定义n的初始值为1
    while n <= 10:#当n<=10是进行本缩进内的循环
        print("{:5d}".format(i * n), end='')  #"{}".format()将（）中的内容输入{},:5d设置打印宽度为5个字节，end=' '将换行符替换为一个空格
        n += 1#将n加1
    print()#上面的n所在的while循环结束后换行
    i += 1#i加上1
print("-" * 50)#打印50个-符号
